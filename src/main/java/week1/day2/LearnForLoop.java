package week1.day2;

public class LearnForLoop {

	public static void main(String[] args) {

		for (int i = 0; i <= 100; i++) {
			System.out.println("The value of i is" + i);
		}
		int i = 0;
		while (i <= 100) {
			System.out.println("The value of i is" + i);
			i = i + 1;
		}

		for (int i1 = 100; i1 >= 0; i1--) {
			// System.out.println("The value of i: "+i);
			i1 = i1 - 1;
		}

		int i1 = 100;
		while (i1 >= 0) {
			System.out.println("the value of i is" + i1);
			i1 = i1 - 2;

			if (i1 % 2 == 0) {
				System.out.println("the value of i is" + i1);
			}
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		

	}

}
